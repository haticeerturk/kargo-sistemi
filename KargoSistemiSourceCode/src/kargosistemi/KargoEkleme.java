    private void jButtonUrunKaydetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUrunKaydetActionPerformed

        String urunTipi;
        double agirlik;
        int adet,ebatX,ebatY;
        
        urunTipi = jTextFieldUrunTipi.getText();
        if(urunTipi.length() > 0 && jTextFieldAgirlik.getText().length() >0 && jTextFieldEbatX.getText().length() >0 && jTextFieldEbatY.getText().length() > 0 && jTextFieldAdet.getText().length() > 0){
            agirlik = new Double(jTextFieldAgirlik.getText());
            ebatX = new Integer(jTextFieldEbatX.getText());
            ebatY = new Integer(jTextFieldEbatY.getText());
            adet = new Integer(jTextFieldAdet.getText());

            Urun yeniUrun = new Urun(mus.mID, urunTipi, agirlik, ebatX, ebatY, adet);
            kaydedici.urunBilgisiGir(yeniUrun);

            int toplamEbat=0;
            double toplamAgirlik = 0.0 ;
            for(int i=0; i<adet; i++){
                toplamEbat += ebatY;
                toplamAgirlik += agirlik;
            }
            
            int ID = (int)kaydedici.sonuncuGetir(yeniUrun);

            Kargo yeniKargo = new Kargo(mus.mID, ID, toplamAgirlik, ebatX, toplamEbat);
            kaydedici.kargiBilgileriGir(yeniKargo);

            jPanelUrunBilgileri.setVisible(false);
            jPanelUrunKargoBilgileri.setVisible(true);
            jPanelAcikEksiltme.setVisible(false);

            jLabelUrunID.setText(Integer.toString(ID));
            jLabelAdet.setText(Integer.toString(adet));
            jLabelAgirlik.setText(String.valueOf(agirlik));
            jLabelUrunTipi.setText(urunTipi);
            jLabelEbat.setText(Integer.toString(ebatX)+" - "+Integer.toString(ebatY));
 
            kID = kaydedici.sonuncuGetir(yeniKargo);
            jLabelKToplamEbat.setText(Integer.toString(ebatX)+" - "+Integer.toString(toplamEbat));
            jLabelKUrunID.setText(Integer.toString(ID));
            jLabelKToplamAgirlik.setText(String.valueOf(toplamAgirlik));
            jLabelKargoID.setText(Integer.toString(kID));
        }
        else{
            jLabelUrunHata.setText("Lütfen ürün Bilgilerini eksiksiz doldurun!");
        }
    }//GEN-LAST:event_jButtonUrunKaydetActionPerformed

