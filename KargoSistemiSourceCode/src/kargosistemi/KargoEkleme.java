    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int ucret;
        String teslimAlinacakAdres, teslimEdilecekAdres, tarihBaslangic, tarihBitis, aeBitisTarih;
        
        teslimAlinacakAdres = jTextFieldTAAdres.getText();
        teslimEdilecekAdres = jTextFieldTEAdres.getText();
        tarihBaslangic = jTextFieldTarihBaslangic.getText();
        tarihBitis = jTextFieldTarihBitis.getText();
        aeBitisTarih = jTextFieldAEBITISTARIH.getText();
        if(teslimAlinacakAdres.length() > 0 && teslimEdilecekAdres.length() > 0 && tarihBaslangic.length() > 0 && tarihBitis.length() > 0 && aeBitisTarih.length() > 0 && jTextFieldUcret.getText().length() > 0){
            ucret = new Integer(jTextFieldUcret.getText());

            AcikEksiltme yeniAcikEksiltme = new AcikEksiltme(mus.mID, kID, ucret, teslimAlinacakAdres, teslimEdilecekAdres, tarihBaslangic, tarihBitis, aeBitisTarih);
            kaydedici.acikEksiltmeBaslat(yeniAcikEksiltme);

            this.setVisible(false);
        }
        else{
            jLabelAEHata.setText("Boş Alanları Doldurunuz!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

